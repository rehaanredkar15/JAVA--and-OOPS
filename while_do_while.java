package com.company;

public class while_do_while
{
    public static void main(String[] args)
    {
        System.out.println("THE WHILE LOOP");
        int i = 0;
        while(i<=10)
        {
            System.out.println(i);
            i++;
        }
        System.out.println("do while loop starts here ");
        i=1;
        do {
            System.out.println(i);
            i++;
        }while(i<=30);
    }

}
